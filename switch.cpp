
#include<iostream>

using namespace std;

int main(){
	
	int numero;
	
	cout<<"Ingrese un  del 1 al 5 : "; cin>>numero;
	
	switch(numero){
		case 1 : cout<<"El numero ingresado es 1"; break;
		case 2 : cout<<"El numero ingresado es 2"; break;
		case 3 : cout<<"El numero ingresado es 3"; break;
		case 4 : cout<<"El numero ingresado es 4"; break;
		case 5 : cout<<"El numero ingresado es 5"; break;
		default : cout<<"numero invalido"; break;
	}
	
	
	
	return 0;
}












